unsigned char statusbar_buffer[STATUS_BAR_SIZE*4];
unsigned char float_text_buffer[FLOAT_TEXT_SIZE*4];
#define TEXT_FRUIT 23
#define TEXT_FRUITS 24
/*
 * copy_to_buffer
 *   DESCRIPTION: copy the string into the status buffer
 *   INPUTS: a pointer to the string, which stores the index to the character
 *           and the alignment method of the string in status bar
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: statusbar_buffer fills with string char
 */
void copy_to_buffer(const char* text, int alignment, int test, int level, int fruit)
{
    int i, row, col;
    int alignment_offset;
    int length = fruit > 1 ? TEXT_FRUITS:TEXT_FRUIT;
    char level_color[10];
    char font_color[10];

    
    for (i = 0; i < 10; i++) {
        level_color[i] = 0x22;
        font_color[i]  = 0x0c + i;
    }
    // if test, fill buffer with color
    if (test) {
        for (i = 0; i < STATUS_BAR_SIZE*4; i++)
            statusbar_buffer[i] = 0x0e;
        return;
    }
    if (alignment == 0) alignment_offset = 0;   // left alignment
    if (alignment == 1) alignment_offset = (STATUS_BAR_WIDTH - length*FONT_WIDTH)/2;    // center
    if (alignment == 2) alignment_offset = STATUS_BAR_WIDTH - length*FONT_WIDTH;        // right
    // fill the buffer with text
    // unsigned char font_data[256][16]
    // Each character is 8x16 pixels
    for (i = 0; i < STATUS_BAR_SIZE*4; i++)
        statusbar_buffer[i] = level_color[level];
    for (i = 0; i < length; i++) {
        int character = (int) text[i];  // the character index to be shown (one of 256)
        for (row = 0; row < STATUS_BAR_HEIGHT-2; row++) { // 16
            int bitmap = 0x80;  // 1000 0000
            for (col = 0; col < FONT_WIDTH; col++) {    //  8
                int p_off = col & 3;
                // calculate index in buffer: (row+1) is for empty line in status bar 
                int id = p_off * STATUS_BAR_SIZE + (row+1) * STATUS_BAR_WIDTH/4 + (i*FONT_WIDTH + col + alignment_offset)/4; 
                if (font_data[character][row] & (bitmap >> col)) 
                    statusbar_buffer[id] = font_color[level];
                //else statusbar_buffer[id] = 0x0e;
            }
        }
    }
    
    return;
}



/*
 * copy_to_floatText
 *   DESCRIPTION: copy the string into the float text buffer
 *   INPUTS: a pointer to the string, which stores the index to the character
 *           and test flag
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: float_text_buffer fills with string char
 *   
 *   background mainly falls on 0x30 - 0x3F and 0x20- 0x23
 */
void copy_to_floatText(const char* text, unsigned char *backgrd_buffer, int fruit, int test)
{
    /* 6-bit RGB (red, green, blue) values for first 64 colors */
    int i, row, col;
    int length = 10;
    // if test, fill buffer with color
    if (test) {
        for (i = 0; i < FLOAT_TEXT_SIZE*4; i++)
            float_text_buffer[i] = 0x0e;
        return;
    }

    // else, store the fruit text into buffer
    //int alignment_offset = (FLOAT_TEXT_WIDTH - length*FONT_WIDTH)/2;    // center
    for (i = 0; i < FLOAT_TEXT_SIZE*4; i++)
        float_text_buffer[i] = backgrd_buffer[i];
    for (i = 0; i < length; i++) {
        int character = (int) fruit_text[fruit][i];  // the character index to be shown (one of 256)
        for (row = 0; row < FLOAT_TEXT_HEIGHT; row++) { // 16
            int bitmap = 0x80;  // 1000 0000
            for (col = 0; col < FONT_WIDTH; col++) {    //  8
                // calculate index in buffer: it is different from status bar buffer, no plane indirection is needed.
                // i am genius 
                int id = row*FLOAT_TEXT_WIDTH + i*FONT_WIDTH + col;
                if (font_data[character][row] & (bitmap >> col)) {
                    //color_semitrans(backgrd_buffer[id]);
                    float_text_buffer[id] = (unsigned char)((int)backgrd_buffer[id] + 64);
                }
            }
        }
    }
    return;
}


